import {existsSync, mkdirSync, readFileSync, writeFileSync} from 'node:fs';
import path from 'node:path';

// import.meta.dirname is always this script's directory — never affected by CWD
const projectRoot = path.join(import.meta.dirname, '..');
const src = path.join(projectRoot, 'CHANGELOG.md');

let content = '';
if (existsSync(src)) {
    content = readFileSync(src, 'utf-8');
} else {
    console.warn('[generate-changelog] CHANGELOG.md not found at', src);
}

const outDir = path.join(projectRoot, 'src', 'generated');
mkdirSync(outDir, {recursive: true});
writeFileSync(
    path.join(outDir, 'changelog-content.ts'),
    `// Auto-generated by scripts/generate-changelog.mjs — do not edit\nexport const CHANGELOG_CONTENT = ${JSON.stringify(content)};\n`
);
console.log('[generate-changelog] wrote src/generated/changelog-content.ts');
